#  any empty list
empty_list = []

# a list of strings
str_list = ['life', 'is', 'beautiful']

# a list of integers
int_list = [1, 1, 2, 3, 5, 8, 13]

# mixed items list
mixed_list = ['This', 9, 'is', 1808, 20.09, 'a', 2022, 'mixed', 1203, 'list']

# to print the list
print(empty_list)
print(str_list)
print(int_list)
print(mixed_list)

# access items in Python list
print(mixed_list[0])
print(int_list[4])

# access the last item
print(str_list[-1])
